// Learning 1 : Basic Syntax of Java Program
public class L1_BasicSyntax { /* 1) every line of code that executes in JAVA must be inside a class
                              - the class can be any name. This time our class name is "basicSyntax" */

    public static void main(String[] args) { /* 2) main() is required in every Java program. Only the codes inside the main() method
                                                   will be executed*/
        System.out.println("Welcome to the journey of learning JAVA.");  // println() method is used to print a line of text to the screen
        System.out.println("Hope you enjoy yeah.");                      // every statement must end with a semicolon ;
    } // we need the curly bracket {} to mark the beginning and the end of a block of code.
}