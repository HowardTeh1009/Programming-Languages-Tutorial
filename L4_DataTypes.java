// Learning 4 : Data Types in Java
/*
    In the previous chapter (Variables), we have understand that a variable must be having a specific data type.
    There are a few data types in Java:
        1) int     - Integer
        2) float   - Floating point number
        3) char    - Character
        4) String  - String
        5) boolean - Boolean
        6) double  - Double point number
*/

public class L4_DataTypes {
    public static void main (String[] args){
        // A list of data types are listed here
        int myNum = 5;               // Integer (whole number)
        float myFloatNum = 5.99f;    // Floating point number
        char myLetter = 'D';         // Character
        boolean myBool = true;       // Boolean
        String myText = "Hello";     // String
    }
}
